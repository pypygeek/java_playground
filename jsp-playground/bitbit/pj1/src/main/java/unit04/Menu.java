package unit04;

public class Menu {
	private String shopMenu;
	private String name;
	private int price;
	
	public Menu() {}

	public String getShopMenu() {
		return shopMenu;
	}

	public void setShopMenu(String shopMenu) {
		this.shopMenu = shopMenu;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}
	
	
}
